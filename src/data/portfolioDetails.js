export function getProjectDetails(slug) {
    return portfolioDetails[slug] ?? null;
}

export function mergeProjectData(project) {
    const details = getProjectDetails(project.slug);
    const techFromSubtitle = project.subtitle
        ? (project.subtitle
              .split("-")
              .pop()
              ?.split("|")
              .map((t) => t.trim())
              .filter(Boolean) ?? [])
        : [];

    return {
        ...project,
        role: details?.role ?? "Developer",
        overview: details?.overview ?? project.desc,
        features:
            details?.features ?? [
                "End-to-end product development",
                "Scalable architecture",
                "Production deployment",
                "Performance optimization",
            ],
        techStack: details?.techStack?.length
            ? details.techStack
            : techFromSubtitle.length
              ? techFromSubtitle
              : [project.category],
        outcomes: details?.outcomes ?? [
            "Delivered on schedule with production quality",
            "Maintainable codebase for long-term growth",
        ],
    };
}
